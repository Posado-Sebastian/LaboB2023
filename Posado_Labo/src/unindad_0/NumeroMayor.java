package unindad_0;

public class NumeroMayor {
    public static void main(String[] args) {
        int n = 1;
        int n2= 1;
        if(n2==n){
            System.out.println("Los dos numeros son iguales");
        }
        else if(n>n2){
            System.out.println("El numero 1 el mayor al numero 2");
        }else{
            System.out.println("El numero 2 el mayor al numero 1");
        }
    }
}
